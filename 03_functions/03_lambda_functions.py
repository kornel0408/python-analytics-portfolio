# ***************************************************
#           Lambda Function
# **************************************************

# Definition:
# A lambda function is a small anonymous function in Python.
# It can have any number of arguments but only one expression.

# SYNTAX:
# lambda arguments : expression


# --------------------------------------------------
# Normal functions Vs Lambda functions
# --------------------------------------------------

def add(a, b):
    return a + b   # Normal function
print(add(5, 3))


add = lambda a, b: a + b

print(add(5, 3))
# ------------------------------------------------- 






# -------------------------------------------------
# 1. Square of Number
# ------------------------------------------------

square = lambda a : a * a
print(square(4))
#------------------------------------------------





# -------------------------------------------------
# 2. Check even number
# ------------------------------------------------

even = lambda num : num % 2 == 0
print(even(6))
# ------------------------------------------------




# -------------------------------------------------
# 3. Maximum of two numbers
# ------------------------------------------------

max = lambda a, b: a if a>b else b
print(max(7,567))
#-------------------------------------------------




# ---------------------------------------------------
#  4. lambda inside print
#----------------------------------------------------

print((lambda a, b : a * b)(5, 6))
print((lambda a: a ** a)(3))
#--------------------------------------------------




# ---------------------------------------------------
# 5. lamda with map
#---------------------------------------------------
numbers = [10, 20, 30]

double = list(map(lambda x: x * 2, numbers))

print(double)
#--------------------------------------------------